public class DemoDouble {
  
  public static void main(String[] args) {
    
    double d1 = 0.2; //If no notation, default it is double value
    double d2 = 0.3; // d is the notation of double
    double d3 = d1 + d2;
    System.out.println(d3); //0.5

    double d4 = 19999.31123912302110109;
    System.out.println(d4);

    float f1 = 0.2f; //f is the notation of float

    float f2 = 19999.3112391230211010f; //19999.31123912302
    System.out.println(f2); // 19999.31123912302
  }
}
